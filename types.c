#include <stdio.h>
#include <limits.h>
#include <float.h>



int main()
{
    printf("The max and min of char is: %d and %d\n", CHAR_MAX, CHAR_MIN);
    printf("The max and min of signed char is: %u and %u\n", SCHAR_MAX, SCHAR_MIN);
    printf("The max and min of unsigned char is: %u and %u\n", SCHAR_MAX, SCHAR_MIN);
    printf("The max and min of long integers is: %ld and %ld\n", LONG_MAX, LONG_MIN);
    printf("...\n");
}
